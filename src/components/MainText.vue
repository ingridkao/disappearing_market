            <h6>尾聲</h6>
            <p>最後一次見到劉先紅是11月9日。在過去的一個月，經營20多年的四環花木市場已全部騰空，一些商戶搬入了“丰台區花鄉新發地電商產業園”，學習“互聯網+批發“的經營模式，但他們學得不太好，每天還是開車給人送貨，”沒精力搞那個。“一位花農說。而留在北京的成本已比一個月前高出了一倍。</p>
            <p>醬料鋪裡，堆滿醬油瓶子的貨櫃落滿了灰老闆周文慧和丈夫每天下午會做些面和大餅。 - 這是生意寂寥後，他們開設的新業務但無論怎樣努力，因為人流銳減，門店依然難以為繼，他們打算明年開春“就收攤不干了”。</p>
            <p>曾經的成壽寺菜市場被徹底抹平，變成停車場馬路。對面，稀稀疏疏的菜農常常在傍晚出沒，沿街賣菜，收入還算穩定，只是每天得心驚膽戰地和城管玩“貓捉老鼠“的遊戲。</p>
            <p>在北京東城區一家大型商場，李會雲找到了一家28平米的鋪面。門面華麗高貴，店內的暖光打在她身上那一刻，她覺得生活的希望又來了。她說自己體會到一個樸素的道理 - “只要努力，老天不會絕了你的路”。</p>
            <p>她一次性付了7萬塊入駐費，“求了個安穩”。新店面租金每年17萬，是過去的3倍，接近她20年攢下的全部存款20萬。“很貴，”但李會雲“顧不得那麼多了”，她再也不想過被人驅趕的生活。入駐那天，她一連發了9條朋友圈，像是迫切宣告勝利。</p>
            <p>11月9日深夜，我和劉先紅經過天緣門口，裡面依然傳出很大聲響。劉先紅說，關閉後一個星期，市場的拆除工作從未停止。一則拍下拆除過程的視頻流進了商戶們的微信群，畫面搖搖晃晃，放眼望去，地上全是碎玻璃渣，木屑，隨意棄擲的商品。“好好的一個市場，現在整個一片廢墟，垃圾！”視頻裡一個聲音說。</p>
            <p>夜裡，琥珀燈光灑在路上，行人形色匆匆。天緣市場斑駁的門上密密麻麻貼著破碎紙張，上面是地址和電話，那是修表店，美甲店等商戶搬走後專門回來貼上的。一場切除運動後，它們成為這座城市商戶和居民們僅有的溝通線索。而更多的商戶則從北京永遠消失了。</p>
            <p>我問劉先紅未來要如何打算。</p>
            <p>他彷彿沒聽見，駐足在天緣門口，忽然感慨起自己倏忽而過的二十年時光：“二十年啊，這幾乎和我在老家的時間差不多了。”</p>
            <p>三天前，一位曾向劉先紅買貨的廣安賓館採購員聽說了他的遭遇，主動提供了賓館地下一樓的廢棄倉庫，此前劉先紅三個倉庫近八萬塊的貨終於有了存放的地方，他為此高興壞了。劉先紅說自己要繼續留在北京，正在尋找新的賣場。“回老家幹嘛呢，我沒地，沒人脈，沒事業，什麼也做不了。”</p>
            <p>夜晚10點半，會面結束，我們打算各自回家。路上行人熙來攘往。“走了！”劉先紅一個箭步跨向馬路中央，雙手在空中揮舞和我告別，一邊走，一邊喊：“我不會回老家的，怎麼樣都不會“。</p>
