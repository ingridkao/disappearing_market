            <h6>在歐洲被極力保存的小市場，正在從北京大規模消失</h6>
            <p>2017年11月20日，醬料店老闆周文慧所在的成壽寺菜市場（位於南四環）突然被拆。三天後的深夜，她在返家途中看見鄰居們慌裡慌張地抱著行李從公寓出來。“原來房東正在趕人”，公寓也已停水停電。周文慧一家三口在接近零度的房間挨了三天，最終還是搬入附近樓房，租金比過去貴一倍。2018年2月，她在原來的菜市場附近找到一間16平米的門店，與人合租。但生意大不如前，由於沒有庫房，她經常無法為客戶配齊貨，客源出現斷崖式下跌，每月收入不到5000元（人民幣，下同）。</p>
