            <h6>合作十多年的12家工廠，全部不見了</h6>
            <p>和超市裡商品掃碼的“物質代理”（編註：即“material delegation”，是指用技術手段或產品管理調控人與人之間的關係）相比，發生在市場裡的交易或許沒有那麼高效，卻積澱了難得的人情味。劉先紅的弟媳李會雲在天緣市場地下二層賣女裝，兩年內歷經四次搬遷，過去的客戶總會主動聯絡她，幫襯生意，“大家講究情分。”</p>
            <p>構建在人情上的市場，才能不太計較商業社會最看重的時間成本和利潤率，去體察個體需求。劉先紅回憶，一位年近七旬的老大爺曾到天緣市場，想買一雙“43碼大，墊子軟，後跟兒高“的鞋。當時，劉先紅正為企業供應橡膠鞋，進貨時，他特地留心為老大爺找到一雙鞋。此後，老大爺一旦有空，就來天緣遛彎兒，主動關心劉先紅的生意劉先紅生病，他還用北京的醫保為劉先紅買過藥劉先紅說，這就是小市場的特色 - “講究人情”。</p>
            <p>而北京的各類市場背後，牽連著京津冀地區一條條所謂“低端”的產業鏈。近幾年，從製造商到市場的全鏈條潰退，亦令劉先紅們感受到了“唇亡齒寒”0.90年代，河北安新縣還未被頂層選中，劃入“雄安新區”時，它以低廉地價，臨近北京的優勢，吸引了一批小商品商在此設廠。劉先紅記得，每逢週末，他清晨五點起床，搭乘最早一班的城際班車到100公里外的河北安新縣和白溝縣進貨，深夜再馱著數十箱貨物返京。生意做了十數年，劉先紅和廠方的信任日久彌堅。劉先紅常常一個電話打過去，對方就將近百箱貨交給司機運至。返程時，劉先紅再拜託司機把貨錢轉交供貨商。“我們都相信誠信經營的法則。”劉先紅說。</p>
            <p>變化發生在2014年。當年4月，中國環保法迎來25年來首次修訂，同一時間，京津冀地區開始大力整治環境污染企業。河北的塑料製品企業無一例外被停工，關閉，貨物供應也因此越來越得不到保證，累及下游像劉先紅這樣的批發商。因為無法向客戶保證供貨數量和時間，一些要貨量大的客戶漸漸流失，“生意就越來越不好做了。 “</p>
            <p>2015年末的一天，劉先紅正在天緣市場地下一間幽暗倉庫裡點貨，桌上的電話突然響起，對方是在安新縣與他合作了多年的塑料廠老闆霍旋，霍旋氣急敗壞地告訴他：“國家對我們這裡有計劃，生意做不下去老劉，我們要搬去內蒙啦。”</p>
            <p>2015年，在習近平的主導下，“京津冀協同發展規劃綱要”出台，要求天津，河北向北京看齊，積極發展新能源智能汽車，雲計算與大數據，新一代移動互聯網等新產業，疏解一般製造企業，批發市場，物流中心。據官方通報，截至2017年9月，28京津冀個城市核查上報的17.6萬家企業被取締關停。劉先紅目睹他合作了十數年的生意夥伴一個接一個從河北離開，“12家工廠，全部不見了！”。</p>
            <p>北京城區的疏解工程也明顯加速。2017年5月開始，劉先紅看著萬通，眾合，天意，天河白馬等市場消失，他當時想，“天緣是地下市場，不佔地，不影響交通，應該不會被關吧。“合作多年的廠商撤了，他就重新尋找新的合作方，從沒打算另尋出路，三個倉庫裡貨物永遠滿滿噹噹。</p>
            <p>但2018年9月27日，天緣市場方突然通知“為貫徹北京產業升級計劃”，市場將在15日內閉市。憤怒的商戶很快組建了維權群，選出代表與管理者談判，談判無果後，大家將市場告至西城區信訪局，也只爭取到延長半個月的甩貨時間。</p>
            <p>10月31日下午六點，被清空的天緣市場，只剩下散落在地上的名片和懸掛在空空貨櫃上，無人閱讀的紙板，上面寫著商家的新地址 - 大多離開了北京，搬到了河北固安，高碑店 - 儘管在那裡也終將難逃被清除的命運。</p>
