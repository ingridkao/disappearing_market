            <h6>政府物業也來他這兒訂貨</h6>
            <p>如今回看，“被迫驅離”是許多外地人在京奮鬥數十年的結局，卻不是他們漫長征途的開始。</p>
            <p>1995年年，北京外來人口數量出現歷史性猛增，一年內從63萬飆升至181萬，到了1997年年，這個數字變成了229萬。他們多半集中在建築，餐飲，服務等行業，撐起了。一座城市的半邊天儘管當局於同一時間推行“暫住證”（編注：“暫住證”是外來人口在城市打工時，由公安機關出具的合法證明，無證人員一經查實會遭到處罰或遣返） ，大規模嚴查無證外來人口，但在就業空間，生存環境顯著改善之際，“北京依然成為人口遷入，充滿人生發展希望之都。”在“北京，上海，2000萬+人口進化史“中，上海知名財經媒體人秦朔曾這樣評價。</p>
            <p>隨著外來人口大規模來京，許多關涉居民日常生活的產業也紛紛搭建起來。商戶們自覺捕捉需求，沿街擺攤，最終聚合成一個人際交流的市場。這背後有一個極容易被忽視的隱患：許多市場並非商業用地，多屬於住宅和農業用地，“所以制度上來說，它們一定是非法的，”盛強說某種程度上，這成為政府如今清退外地人常用的合法性。</p>
            <p>但在1990年代，為了促進經濟發展，面對湧入的外地人和沿街搭建的市場，政府更像一雙沉默注視的眼睛，讓他們生根，發芽，向城市源源不斷地“輸血”。</p>
            <p>據“經濟觀察報”報導，1992年左右，北京開始出現各類市場，業界一度流傳：“賣眼鏡的集中在朝陽區，西四賣電子產品，丰台大紅門做服裝生意”，豐富的商業形態形成了。彼時，面對自組織的市場，政府的態度普遍友好。北京開放了營業執照的辦理，將商戶們合法化，變相推動了專業化賣場的建成。</p>
            <p>1997年年成立的天緣市場就是這樣發展起來的。由於附近有四所小學，天緣甫一成立，就迅速吸引了一批經營文具用品的小商販，他們主要來自湖北，安徽和浙江。在市場的調節下，商戶們順應周邊社區居民的需求，調整經營內容。很快，一個集文具，服裝，音響設備業務於一體的小型市場形成了。同年，劉先紅也繳下入駐費，成為這裡第一批進駐的17號商戶。</p>
            <p>劉先紅今年55歲，高鼻樑，尖下巴，鬍子拉碴，臂膀粗壯。少年時期，劉先紅在湖北省監利縣跟著爺爺學做麻糖。機器在他耳邊隆隆轉，整日打交道的是芝麻，糯米和麥芽。</p>
            <p>後來，他告別製糖的鍋爐，在當地學做小生意“得讓自己有用些。”劉先紅說他賣過麻糖，挨家挨戶用地瓜換糧票（編注：。上世紀50至80年代物資緊缺時期，由政府發放的一種購糧憑證），最遠一次，還裝上兩桶麻糖廠用剩的塑料手套，從武漢十九碼頭乘船到南京叫賣。或許是因為太瘦，他被當地人誤當成小偷扭到派出所。警察搜身時，他一路賺來的700多元錢從衣服口袋裡掉下來。案子查清後，他被釋放，錢卻被警察拿走了。</p>
            <p>“真的是什麼都做過了，可就是賺不到錢啊。”劉先紅苦笑著嘆了口氣。</p>
            <p>1994年年春節，妹夫羅為想碰巧從北京回湖北，意氣風發的樣子深入人心。家族裡，羅為想是最早來京經商的人，大家公認的“有遠見”，“思想開放”之人，每次過年，羅為想都拿上四五千元鈔票回家。</p>
            <p>那一年，中國大陸的個體戶（編注：是以個人財產或者家庭財產作為資本經商的人）數量開始猛增，到1999年，全國范圍內的個體戶超過三千萬，達至改革開放以來的巔峰。</p>
            <p>羅為想在北京經營塑料製品，生意正隆，出貨量快，平均一周就要外出進一次貨，無人看攤。他想僱個信得過的人，便打算拉為人老實的劉先紅一把“。老劉，北京大，跟我去北京吧！“在那個普遍缺乏生財渠道的年代，鄉村大多數人就是通過”幫帶“的形式告別鄉土，湧往都市的。</p>
            <p>機會意外而至，劉先紅卻沉默了。他的一雙兒女還在念小學。“走了，覺得對不住孩子。”可不走，夫妻倆又太窮。最後，劉先紅向親戚們借了2000塊，春節一過，攜妻子雙雙北上了。</p>
            <p>最開始，劉先紅與妹夫羅為想在西北二環的淨土寺附近租下一個門面，經營服裝輔料。鐵棚下，人流來來往往，生意很快上了軌道。</p>
            <p>勤奮，吃苦是許多外地商戶來京立身之本。當時，除了白天經營門店的8個小時外，劉先紅與羅為想兩人還奔忙於早晚街市，每天睡眠不足四小時。</p>
            <p>但那卻是令人覺得充滿希望的日子。劉先紅回憶，僅僅憑做早晚市，他一年淨收入就有四五千元，彼時家鄉湖北的人均年收入不過783元。有了錢，生活也好了起來。劉先紅夫婦不僅在老家建了房，還把一對兒女接來北京讀書，一家人終於團圓了。回看二十年，劉先紅說，“在北京，比在老家強太多了！“</p>
            <p>1997年年，劉先紅進駐天緣市場，由於沒有其他領域的積累，只能繼續經營服裝輔料，但附近沒有相關的上下游產業，他的貨櫃常常門可羅雀。</p>
            <p>“必須轉變思路。”劉先紅記得，1998年，幾家大型酒店在天緣市場附近拔地而起。年中，一位酒店採購員來到天緣，一次將劉先紅攤位上的百潔布全數買走。</p>
            <p>劉先紅果斷放棄服裝業，轉做保洁用品，並主動為客戶配全商品，送貨上門，憑藉這一優勢，他很快穩固了競爭力。此後，訂單滾滾而來，除了大型酒店，百貨市場也會派人來訂貨。</p>
            <p>更高端的客戶也有。 - 中水物業管理公司這是北京頂級的物業管理公司，專為政府辦公樓，寫字樓，大型公共建築提供物業服務劉先紅的訂單中有水利部，中宣部，最高人民檢察院，團中央，宣武區人民政府等一系列政府機構的名字，並服務2002年至今，年，劉先紅還被彼時的宣武區人民政府評為“先進商戶”，十數年來，他的照片一直被掛在在天緣進門右手邊的光榮牆上。</p>
            <p>劉先紅統計過，與他有生意往來的酒店，寫字樓和各級政府機構最少有300家，手寫的訂單有80個筆記本之多。採訪行到一半，他從隨身攜帶的跨包裡掏出一個筆記本，紙張破舊，其中一頁記錄：“中紀委服務中心欠賬29715”，“上個月的賬還沒結清呢。”劉先紅哈哈幾聲笑。</p>
